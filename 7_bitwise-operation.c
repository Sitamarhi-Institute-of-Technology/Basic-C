#include <stdio.h>

int main() {

    // BITWISE AND OPERATOR &
    int a = 12, b = 25;
    printf("Output = %d", a & b);

    // BITWISE OR OPERATOR |
    printf("Output = %d", a | b);

    // BITWISE XOR (EXCLUSIVE OR) OPERATOR ^
    printf("Output = %d", a ^ b);

    // BITWISE COMPLEMENT OPERATOR ~

    return 0;
}