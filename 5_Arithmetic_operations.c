#include<stdio.h>
int main()
{
    // THE ARITHMETIC OPERATION WE DO IN MATHEMATICS, WE NEED TO PERFORM SIMILAR OPERATION IN OUR PROGRAMS TO SOLVE QUESTIONS.
    // TO PERFORM MATHEMATICAL OPERATION WE USE ARITHMATIC OPERATIONS
    
    double num1,num2,num3;

    printf("\nENTER NUM1 = ");
    scanf("%lf",&num1);

    printf("\nENTER NUM2 = ");
    scanf("%lf",&num1);

    // SUMMATION (+)
    num3 = num1 + num2;

    printf("\n%lf + %lf = %lf",num1,num2,num3);

    // SUBTRACT (-)
    num3 = num1 - num2;

    printf("\n%lf - %lf = %lf",num1,num2,num3);

    // MULTIPLY (*)
    num3 = num1 * num2;

    printf("\n%lf * %lf = %lf",num1,num2,num3);









    return 0;
}